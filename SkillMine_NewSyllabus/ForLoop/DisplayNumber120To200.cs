using System.Text;

namespace SkillMine_NewSyllabus.ForLoop
{
    class DisplayNumber120To200
    {
        static void Main(string[] args)
        {
            for (int i = 120; i <= 200; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
